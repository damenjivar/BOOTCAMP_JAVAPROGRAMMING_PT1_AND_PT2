package com.bootcamp.pt1.easy_programming_questions;

public class Q3_Easy_Java_Programming {

	// Q3: Write a Java program to find the average of three numbers.

	public static void main(String[] args) {
		int num1 = 10;
		int num2 = 15;
		int num3 = 5;
		int averageSum = (num1 + num2 + num3) / 3;
		System.out.println("Average sum of these three numbers: " + averageSum);
	}
}